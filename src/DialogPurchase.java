    private void txtCodeIdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodeIdKeyReleased
        try{
            String barCode = txtCodeId.getText();
            
            
            ResultSet rs = db.runSelect("SS_ITEMTYPE", "*", "BARCODE = '"+barCode+"'", "");
            if(rs.next()){
                txtItem.setText(rs.getString("itemName"));
                txtUnit.setText(rs.getString("units"));
            }
            Date date = new Date();
            dcDate.setDate(df.parse(df.format(date).toString()));
            
        }catch(Exception exp){
            JOptionPane.showMessageDialog(null,"Errro:"+exp.getMessage());
        }
    }//GEN-LAST:event_txtCodeIdKeyReleased
