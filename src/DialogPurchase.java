    private int getQty(String item) throws Exception{
        String tableName = "ss_stocked";
        String columns = "*";
        String where = "itm_name = '"+item+"'";
        String order = "";
        int qty = 0;
        ResultSet rs = db.runSelect(tableName, columns, where, order);
        if(rs.next()){
            qty = rs.getInt("itm_qty");
        }
        return qty;
    }
    
    public void loadTable(){
        try{
            String colTit[]     = {"ID","ITEM NAME","DATE","TIME","UNIT","QTY"};
            String colRow[]     = {"ID","ITM_NAME","DATE","TIME","ITM_UNIT","ITM_QTY"};
            ResultSet rs        = db.runSelect("ss_stocked", "*", "", "");
            
            DefaultTableModel model = db.fillTable(rs, colRow, colTit);
            tbPurchase.setModel(model);
            
            
        }catch(Exception exp){
            exp.printStackTrace();
        }
    }

