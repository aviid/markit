    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            //Form variables
            String expenses = txtExpenses.getText();
            String eDate = df.format(dcDate.getDate());
            String amount = txtAmount.getText();
            
            //Table variable for insert function
            String tableName = "SS_EXPENSES";
            String columns[] = {"EX_EXPENSES","EX_AMOUNT","EX_DATE"};
            String values[] = {expenses,amount,eDate};
            
            //Table variable for loading content
            
            
            
            //Condition for expenses insertion
            if(expenses.isEmpty() || eDate.isEmpty() || amount.isEmpty()){
                lbMessage.setText("One or  more field empty");
            }else{
                db.runInsert(tableName, columns, values);    
                loadTable();
            }
        }catch(Exception exp){
            JOptionPane.showMessageDialog(null, exp.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            
            int row = tbExpenses.getSelectedRow();
            int col = 0;
            String id = tbExpenses.getValueAt(row, col).toString();
            
            String tableName = "SS_EXPENSES";
            String where = "EX_ID = "+id;
            
            
            int ck = JOptionPane.showConfirmDialog(rootPane, "You really want to delete this");
            
            if(ck==JOptionPane.OK_OPTION){
                db.runDelete(tableName, where);
                loadTable(); 
            }
                    
        }catch(Exception exp){
            JOptionPane.showMessageDialog(rootPane,"Please select a row table");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

