    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String item = txtItem.getText();
            String unit = txtUnit.getText();
            int newQty = Integer.parseInt(txtQty.getText());
            String tDate = df.format(dcDate.getDate()).toString();
            String barCode = txtCodeId.getText();
//            JOptionPane.showMessageDialog(rootPane,""+getQty(item));
            String tableName = "ss_stocked";
            String columns = "*";
            String where = "itm_name = '"+item+"'";
            String order = "";
            String time  = tf.format(new Date());
            
            int qty = 0;
            
            
            ResultSet rs = db.runSelect(tableName, columns, where, order);
            if(rs.next()){
                //get the quatity and add to it and then upadate
               // JOptionPane.showMessageDialog(rootPane,item+" does exist ");
                
                qty = (getQty(item)+newQty);
                
                String cols[] = {"itm_qty","time"};
                String vals[] = {""+qty,time};
                String whe = "itm_name = '"+item+"'";
                db.runUpdate(tableName, cols, vals, whe);

            }else{
                //inset new values into the freezers
                //JOptionPane.showMessageDialog(rootPane,item+" does not exist ");
                String cols[] = {"itm_name","itm_unit","itm_qty","date","time","itm_barcode"};
                String vals[] = {item,unit,""+newQty,tDate, time, barCode};
                db.runInsert(tableName, cols, vals);
                
            } 
            
        }catch(Exception exp){
            JOptionPane.showMessageDialog(rootPane, ""+exp);
        }finally{
            loadTable();
            txtQty.setText("");
            
            txtCodeId.setText("");
            txtUnit.setText("");
            txtItem.setText("");
            txtCodeId.requestFocus();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

