    public void loadTable(){
        try{
            
            String monts = cbMonths.getSelectedItem().toString();
            String yer = cbYear.getSelectedItem().toString();
            //Table variable
            
            String tableName = "SS_EXPENSES";
            String colums = "*";
            String where = "ex_date LIKE '%"+monts+"%' AND ex_date LIKE '%"+yer+"%'";
            String order = "";

            ResultSet rs = db.runSelect(tableName, colums, where, order);
            
            //ResultSet for total calculation
            ResultSet rss = db.runSelect(tableName, colums, where, order);

            String[] colTitle = {"ID","EXPENSES","AMOUNT","DATE"};
            String[] columns = {"ex_id","ex_expenses","ex_amount","ex_date"};

            DefaultTableModel model = db.fillTable(rs, columns, colTitle);
            tbExpenses.setModel(model); 
            
            double exTotal = 0;
            while(rss.next()){
                exTotal+=rss.getDouble("ex_amount");
            }
            
            txtTotal.setText(""+exTotal);
        }catch(Exception exp){
            JOptionPane.showMessageDialog(rootPane, exp.getMessage());
        }
    }
