    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            int row = tbItems.getSelectedRow();
            int col = 0;
            String id = tbItems.getValueAt(row, col).toString();
            
            String tableName1 = "ss_itemtype";
            String where1 = " ss_id = "+id;
            
            String id2 = tbItems.getValueAt(row, 1).toString();
            String tableName2 = "SS_STOCKED";
            String where2 = " ITM_NAME = '"+id2+"'";
            
            int ck = JOptionPane.showConfirmDialog(rootPane,"Want to delete this item?");
            if(ck==JOptionPane.OK_OPTION){
                db.runDelete(tableName1, where1);
                db.runDelete(tableName2, where2);
                loadTable();
            }
            
        }catch(Exception exp){
            JOptionPane.showMessageDialog(rootPane, "ERROR"+exp.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void tbItemsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbItemsMouseClicked
        try{
            //Table Variables
            int row = tbItems.getSelectedRow();
            int col = 0;
            String id = tbItems.getValueAt(row, col).toString();
            
            //Select table Variable
            String tableName = "ss_itemType";
            String colums = "*";
            String where = "ss_id = "+id;
            String order = "";
            
            ResultSet rs = db.runSelect(tableName, colums, where, order);
            while(rs.next()){
                txtItemName.setText(rs.getString("itemName"));
                txtId.setText(rs.getString("ss_id"));
                txtCostPrice.setText(rs.getString("costPrice"));
                txtSellPrice.setText(rs.getString("sellingPrice"));
                cbVendor.setSelectedItem(rs.getString("vendor"));
                cbUnits.setSelectedItem(rs.getString("units"));
            }
            
        }catch(Exception exp){
            JOptionPane.showMessageDialog(rootPane,"ERROR:"+exp.getMessage());
        }
    }//GEN-LAST:event_tbItemsMouseClicked

