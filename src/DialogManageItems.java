    public void loadTable(){
        try{
            String tableName = "ss_itemtype";
            String cols = "*";
            String where = "";
            String order = "";
            
            ResultSet rs = db.runSelect(tableName, cols, where, order);
            String columns[] = {"ss_id","itemName","costPrice","sellingPrice","units"};
            String colTitle[] = {"ITEM ID","ITEM NAME","COST PRICE","SELLING PRICE","UNIT"};
            DefaultTableModel model = db.fillTable(rs, columns, colTitle);
            
            tbItems.setModel(model);
            
        }catch(Exception exp){
            JOptionPane.showMessageDialog(null,""+exp.getMessage());
        }
    }
    
    private void loadVendor(){
        try{
            String tableName = "ss_vendor";
            String columns = "*";
            String where = "";
            String order = "";
            
            ResultSet rs = db.runSelect(tableName, columns, where, order);
            while(rs.next()){
                cbVendor.addItem(rs.getString("ved_name"));
            }
        }catch(Exception exp){
            JOptionPane.showMessageDialog(null,""+exp.getMessage());
        }
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            if(txtItemName.getText().isEmpty()){
                lbMessages.setText("Item Name is Empty");
            }else if(txtSellPrice.getText().isEmpty()){
                lbMessages.setText("Selling Price is Empty");
            }else if(txtCostPrice.getText().isEmpty()){
                lbMessages.setText("Cost price is Empty");
            }else if(txtCodeId.getText().isEmpty()){
                lbMessages.setText("Code ID is Emtpy");
            }else{
                String itemName     = txtItemName.getText();
                String costPrice    = txtCostPrice.getText();
                String sellingPrice = txtSellPrice.getText();
                String vendor       = cbVendor.getSelectedItem().toString();
                String units        = cbUnits.getSelectedItem().toString();
                String codeId       = txtCodeId.getText();
                //run Insert
                
                String tableName = "ss_itemtype";
                String cols[] = {"itemName","costPrice","SellingPrice","vendor","units","Barcode"};
                String rows[] = {itemName,costPrice,sellingPrice,vendor,units,codeId};
                if(txtId.getText().isEmpty()){
                   
                    db.runInsert(tableName, cols, rows);
                    JOptionPane.showMessageDialog(rootPane,"Record Saved Successfully");
                    loadTable();
                    clearForm();
                }else{
                    
                    String id = txtId.getText();
                    String where = "ss_id = "+id;
                    
                    db.runUpdate(tableName, cols, rows, where);
                    JOptionPane.showMessageDialog(null, "Record Updated Successfully");
                    loadTable();
                    
                }
            }
        }catch(Exception exp){
            JOptionPane.showMessageDialog(null,""+exp.getMessage());
        }finally{
            //txtCodeId.requestFocus();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

